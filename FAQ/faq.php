        <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseTwo1" aria-expanded="false" aria-controls="flush-collapseTwo1">
          Por que existem dois calendários: um do IPVA e outro do documento do veículo (CRLV)?
        </button>
      </h2>
      <div id="flush-collapseTwo1" class="accordion-collapse collapse" data-bs-parent="#accordionFlushExample1">
        <div class="accordion-body">O calendário do Licenciamento/IPVA traz as datas para pagamento à vista (cota única) ou parcelado dos tributos que compõem o Licenciamento Anual do Veículo, com destaque para o Imposto sobre Propriedade de Veículos Automotores (IPVA).

          Só após o pagamento dos tributos que compõem o Pacote do Licenciamento Anual, o documento do veículo (CRLV) é liberado para emissão, desde que não haja restrição veicular administrativa ou jurídica.

          Já o calendário do CRLV diz respeito ao prazo de validade do documento do veículo: o prazo-limite para circular com o CRLV do ano anterior.

          Enquanto o CRLV 2021 estiver dentro do prazo de validade, não pode ser exigido pelos agentes de trânsito o CRLV 2022.

          É possível dirigir com o CRLV 2021, desde que ele esteja dentro do prazo de validade. Isso independentemente de o IPVA estar em atraso.</div>
      </div>
    </div>
    <div class="accordion-item">
      <h2 class="accordion-header">
        <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseThree1" aria-expanded="false" aria-controls="flush-collapseThree1">
          Estou com o IPVA atrasado. Posso ser multado por isso? Meu veículo corre o risco de ser removido para o depósito?
        </button>
      </h2>
      <div id="flush-collapseThree1" class="accordion-collapse collapse" data-bs-parent="#accordionFlushExample1">
        <div class="accordion-body">Não existe multa de trânsito para o atraso no pagamento do IPVA. O veículo também não pode ser removido por atraso no pagamento do IPVA.

          A multa é aplicada quando a pessoa dirige com o documento do veículo fora do prazo de validade. Isso porque, nesse caso, o veículo está com o licenciamento anual irregular. Nessa situação, o veículo pode ser removido e só será liberado depois que estiver devidamente licenciado.</div>
